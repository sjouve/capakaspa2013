	  <h3>Phase 1 (Extrait du <a href="http://blog.capakaspa.info">Blog</a>)</h3>
      <p>Il s'agit d'un joueur débutant qui cherche à assimiler les règles du jeu et le mouvement des pièces. Il n'est pas encore temps pour lui d'aborder la théorie des ouvertures et d'entrer dans les méandres des variations. Je crois qu'à ce moment il faut surtout respecter juste les 3 principes qui doivent <b>orienter le choix des coups</b> :</p>
      <ul>
	  <li>développer ses pièces,
	<li>contrôler le centre,
	<li>mettre son roi à l'abri.
	</ul>
	<P>Mais chacun de ces points posent déjà une multitude de questions. Essayons de les éclaircir en restant sur des idées simples.</P>
	<P>Celui qui pose sûrement le moins de problèmes est le dernier : <b>mettre son roi à l'abri</b>. Comme le but du jeu est de mater le roi adverse, ne laissons pas notre roi à la porter des pièces adverses. Mais comment mettre le roi à l'abri ? En jouant le petit ou le grand roque.</P>
	<P>L'une des conditions pour pouvoir réaliser les roques est que le Fou et Cavalier ne soient plus sur leur position initiale. Pour cela il faut donc <b>développer ses pièces</b>. Elles auront plus de force et contrôlerons plus de cases que sur la première ou dernière rangée. Elles seront donc plus menaçante pour l'adversaire.</P>
	<P>Pour comprendre simplement le <b>contrôle du centre</b>, posons une question liée au point précédent : où est-ce qu'une pièce développée contrôle le plus de cases ? Au centre. Prenons l'exemple du Cavalier, placé sur la colonne 1 ou 8 il ne contrôlera que 4 cases au maximum, au centre il pourra en contrôler le double. Mais comment contrôler le centre ? En tendant vers la position idéale avec 2 pions en e4 ou d4 (pour les blancs). Ici ils empêchent justement les pièces adverses de se centraliser.
<br />Tout cela explique d'ailleurs pourquoi les coups 1.e4 et 1.d4 sont les plus prisés.
</P>
	<P>Sans entrer dans la théorie des ouvertures mais en respectant ces 3 règles, le joueur débutant sera guider dans le choix de ses coups d'ouverture.</P>
	
